import type { DramaState } from "../types";
import type { LLMMessage } from "../../llm/types";
import { SYSTEM_PERSONA, contextBlock, refsBlock } from "./_shared";
import { describeBudget } from "../episode-budget";

export interface CreativeContext {
  /** 一句话题材或完整立项卡（优先取立项卡） */
  brief: string;
  /** 用户在 /start 阶段填的自由输入，比如"先婚后爱+职场双强+年下" */
  freeText?: string;
}

const ANTI_DRIFT = `【防串味要求（必须逐条遵守）】
- 不要把非现代题材写成现代网感剧。
- 不要把高概念世界写成短视频平台表达。
- 不要把特定时代背景写成通用都市语言。
- 不要把世界观内部冲突，偷换为现代舆论或媒体机制。
- 不要让角色说出不属于其时代、身份、阶层、体系的话。
- 不要让重大场景套用与题材无关的模板化场合。
- 所有冲突推进必须优先依赖该世界内部已有制度与习惯，而不是现代观众熟悉的便捷表达。`;

const OUTPUT_TEMPLATE = `# 三幕创意方案

## 一、题材与三幕大纲
剧名：{{剧名（必须一眼能记住、带冲突或反差）}}
受众：{{女频 / 男频 / 全年龄}}
故事类型：{{题材组合，例：先婚后爱+职场双强+年下}}
故事背景：{{市场（国内/海外）+ 时代/空间}}

**Act 1：**
{{一段话，包括：开场爆点（30 秒内成立）、主要人物关系、主角当前处境与压迫来源、第一轮公开羞辱/重大误会/危机事件、主角如何进入故事、第一阶段小反转、Act 1 结尾钩子}}

**Act 2：**
{{一段话，包括：主角如何追查/逃避/误判、敌人如何升级、真相如何逐步揭示、男女主关系如何升级、反派如何持续压迫、外部冲突+内部情感双线加码、中段最关键大反转（身份/孩子/血统/证据/婚姻真相/家族真相二选一）、主角是否开始反击、Act 2 结尾钩子——必须是全剧最密集、足以推动付费的大爆点}}

**Act 3：**
{{一段话，包括：真相如何揭晓、最终对决发生在什么公开场合、反派如何被打脸或失势、男女主关系如何完成回收、权力+身份+爱情三线合一、终局爽点是什么。必须有公开打脸、情感回收、权力关系重置、反派处罚明确、核心谜团全部揭晓、不留第二季钩子。}}

## 二、世界观设定
- 空间与时代：{{一句话}}
- 底层规则 / 制度：{{2-3 条，冲突必须依赖这些规则，而不是现代便捷表达}}
- 主要阵营：{{列 2-4 个，给一句话定位}}
- 核心身份反差：{{一句话，说清谁掩藏了什么真实身份}}

## 三、视觉基调
- 色彩：{{一句话，例：冷调蓝黑 + 红金高光}}
- 光影：{{一句话}}
- 造型与道具亮点：{{2-3 个关键视觉符号}}
- 投流首图建议：{{一句话描述封面定格}}

## 四、核心主题
- 一句话主题：{{必须和爽点绑定，例：底层被制度当作祭品，最终用同一套制度反噬制度本身。}}
- 情绪母题：{{2-3 个关键词}}
- 付费感来源：{{一句话，说明这部戏的「观众愿意付钱看」的核心心理驱动}}

## 五、Optional Upgrade
用 3-5 条要点，说明还能如何强化：爆款感 / 差异化 / 狗血度 / 情绪浓度 / 投流点击率。每条一句话、具体到事件或画面。`;

export function buildCreativeMessages(
  state: DramaState,
  ctx: CreativeContext,
  refs: string
): LLMMessage[] {
  const user = [
    "【任务】你是一名擅长海外/国内竖屏短剧市场的商业编剧策划。",
    "请根据下面的立项信息，输出一个**强投流感、强付费感、强反转**的三幕创意方案。",
    "请不要直接写完整剧本，也不要直接写分集。**只输出三幕创意方案**（供合作伙伴确认）。",
    "",
    "【项目基础信息】",
    contextBlock(state),
    `单集时长：${describeBudget(state.mode)}`,
    "",
    ctx.freeText ? "【用户原始一句话题材/关键词】" : "",
    ctx.freeText ?? "",
    "",
    "【立项参考（立项卡或关键材料）】",
    (ctx.brief || "（用户只给了一句话题材，请自行延展世界观与关键设定）").slice(0, 3000),
    "",
    refsBlock(refs),
    "",
    ANTI_DRIFT,
    "",
    "【写作原则】",
    "- 一切以短剧商业表达为优先，不要写得像传统电视剧简介。",
    "- 不要平均用力，要突出爆点、羞辱、反转、打脸、护短、真相揭晓。",
    "- 语言要具备平台宣传感、策划感。",
    "- 优先写出「观众会立刻想看下一集」的结构。",
    "- 不留第二季钩子；结尾必须闭环；反派处罚明确；核心谜团全部揭晓。",
    "",
    "【输出格式】严格按下面的 Markdown 模板输出，不要外层代码块、不要任何前后说明文字：",
    "",
    OUTPUT_TEMPLATE,
  ]
    .filter((l) => l !== "")
    .join("\n");

  return [
    { role: "system", content: SYSTEM_PERSONA },
    { role: "user", content: user },
  ];
}
