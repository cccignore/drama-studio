const COMPLEX_REVERSAL_RULES = `【复杂反转模式（必须遵守）】
- 三幕合计写出 5–7 层反转的具体揭晓时刻；少于 5 层视为不及格。
- 不要在文中标"第 X 层反转"，反转必须通过情节自然展开。
- 不允许同类反转连续叠（例如 Act 1 与 Act 2 都是身份反转）。
- 终极反转**不得让观众觉得前面都白看了**：要让人想重看，而不是被骗。
- 第一主角必须严格写出 5 要素（年龄 / 外貌 / 穿着 / 气质 / 整体印象），不允许"高冷霸总""美丽善良"这种无法成像的描写。
- 海外向：剧名必须为全大写英文，4 词以内最稳；本土化人名 / 城市 / 阶层冲突，去掉强中国地域设定。
- 短剧必须闭环，不留第二季钩子。`;

export function buildCreativeMessages(
  state: DramaState,
  ctx: CreativeContext,
  refs: string
): LLMMessage[] {
  const isComplexReversal = !!ctx.complexReversal;
  const taskHeader = isComplexReversal
    ? "【任务】你是一名擅长海外短剧 / 高概念反转剧的商业编剧策划，长期为 ReelShort / DramaBox / Dreame 这类平台开发**多层反转密度**的短剧创意。"
    : "【任务】你是一名擅长海外/国内竖屏短剧市场的商业编剧策划。";
  const taskBody = isComplexReversal
    ? "请根据下面的立项信息，输出一个**含 5–7 层反转密度**、强付费感、强投流感的三幕创意方案。**只输出三幕创意方案**，不要直接写完整剧本或分集。"
    : "请根据下面的立项信息，输出一个**强投流感、强付费感、强反转**的三幕创意方案。请不要直接写完整剧本，也不要直接写分集。**只输出三幕创意方案**（供合作伙伴确认）。";

  const user = [
    taskHeader,
    taskBody,
    "",
    "【项目基础信息】",
    contextBlock(state),
    `单集时长：${describeBudget(state.mode)}`,
    "",
    ctx.freeText ? "【用户原始一句话题材/关键词】" : "",
    ctx.freeText ?? "",
    "",
    "【立项参考（立项卡或关键材料）】",
    (ctx.brief || "（用户只给了一句话题材，请自行延展世界观与关键设定）").slice(0, 3000),
    "",
    refsBlock(refs),
    "",
    ANTI_DRIFT,
    "",
    isComplexReversal ? COMPLEX_REVERSAL_RULES : "",
    "",
    "【写作原则】",
    "- 一切以短剧商业表达为优先，不要写得像传统电视剧简介。",
    "- 不要平均用力，要突出爆点、羞辱、反转、打脸、护短、真相揭晓。",
    "- 语言要具备平台宣传感、策划感。",
    "- 优先写出「观众会立刻想看下一集」的结构。",
    "- 不留第二季钩子；结尾必须闭环；反派处罚明确；核心谜团全部揭晓。",
    "",
    "【输出格式】严格按下面的 Markdown 模板输出，不要外层代码块、不要任何前后说明文字：",
    "",
    isComplexReversal ? COMPLEX_REVERSAL_TEMPLATE : OUTPUT_TEMPLATE,
  ]
    .filter((l) => l !== "")
    .join("\n");

  return [
    { role: "system", content: SYSTEM_PERSONA },
    { role: "user", content: user },
  ];
}
