import type { DramaState } from "../types";
import type { LLMMessage } from "../../llm/types";
import { SYSTEM_PERSONA, contextBlock, refsBlock } from "./_shared";

export function buildCharactersMessages(
  state: DramaState,
  startCard: string,
  plan: string,
  refs: string
): LLMMessage[] {
  const user = [
    "【任务】根据立项卡与节奏规划，产出完整的**人物卡 + 人物关系图**。",
    "",
    "【立项信息】",
    contextBlock(state),
    "",
    "【立项卡摘要】",
    startCard.slice(0, 1500) || "（暂缺）",
    "",
    "【节奏规划摘要】",
    plan.slice(0, 1500) || "（暂缺）",
    "",
    refsBlock(refs),
    "",
    "【输出要求】严格输出下列 Markdown；不要包裹在代码块里。",
    "",
    "# 人物设计",
    "",
    "## 一、主要人物卡",
    "",
    "输出 **4–6 张**人物卡，**必须**包含：",
    "- 男主 / 女主（一正一负方视角）",
    "- 1 个中反派（或多面势力核心）",
    "- 1 个大反派",
    "- 0-2 个功能性配角（闺蜜 / 对照组 / 萌宝 / 师父等）",
    "",
    "每张卡片用如下二级标题结构（所有字段必填）：",
    "",
    "### 角色N · 姓名（身份一句话）",
    "- **定位**：男主 / 女主 / 中反派 / 大反派 / 配角",
    "- **年龄 / 职业 / 核心标签**：",
    "- **外显人设**（观众第一眼的印象）：",
    "- **内核 / 秘密**（藏在后面的真相）：",
    "- **弧光**（全剧三段式变化：起点 → 转折 → 结局）：",
    "- **高光戏份**（2-3 个必写的名场面，注明大致集数）：",
    "- **台词风格**（附 1-2 句代表台词，用引号包裹）：",
    "",
    "## 二、人物关系图（Mermaid）",
    "",
    "使用 mermaid flowchart 或 graph，**必须用 ```mermaid 代码块包裹**（这是唯一允许的代码块）。",
    "节点用英文字母+数字 id（如 A1 / B2），用 `Id[中文姓名（定位）]` 声明节点。",
    "边上标注关系（恋人 / 宿敌 / 父女 / 师徒 / 合伙 / 隐藏血缘等）。",
    "",
    "示例格式：",
    "```mermaid",
    "graph TD",
    "  A1[陈默（男主·退伍战神）] -- 夫妻 --> B1[林薇薇（女主·设计师）]",
    "  A1 -- 宿敌 --> V1[陆天佑（大反派·陆氏集团）]",
    "  B1 -- 表姐妹 --> C1[苏小雨（闺蜜）]",
    "```",
    "",
    "要求：",
    "1) 节点数量与上面的人物卡数量一致。",
    "2) 关键「隐藏关系」（如隐藏血缘/卧底身份）用虚线 `-.->` 标注，并在边标签里写 `隐藏` 字样（不要加括号）。",
    "3) mermaid 必须是**合法语法**：中文放在节点方括号内；边标签**不得包含括号、斜杠、引号、管道符等特殊字符**，如需使用请改成自然词（例：把 `战友/核心搭档` 写成 `核心搭档`）；边标签内不得换行。",
    "4) 所有边标签只能是纯中文或英文字母，禁止使用任何 `()（）/\\\"'|` 符号。",
  ].join("\n");

  return [
    { role: "system", content: SYSTEM_PERSONA },
    { role: "user", content: user },
  ];
}
